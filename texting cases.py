random_case_number = random.randint(1, 5)

if random_case_number == 1:
    print("it chose 1")
elif random_case_number == 2:
    print("it chose 2")
elif random_case_number == 3:
    print("it chose 3")
elif random_case_number == 4:
    print("it chose 4")
else:
    print("it chose 5")

print(random_case_number)

